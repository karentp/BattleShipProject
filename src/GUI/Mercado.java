    private void btn_ComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ComprarActionPerformed
       int totalPagar= Integer.parseInt(totalTField.getText());
       int cantidadComprar= Integer.parseInt(cantidadTField.getText());
       int disponible;
       Componentes tipoPago;
       Componentes componente= enumComponenteActual;

       int cantidadActualStock=cliente.getComponentesHashTable().get(componente);
       
       if(!pagaConAcero){
           disponible=cliente.getComponentesHashTable().get(Componentes.DINERO);
           tipoPago= Componentes.DINERO;
           
       }
       else{
           disponible=cliente.getComponentesHashTable().get(Componentes.ACERO);
           tipoPago=Componentes.ACERO;
       }
       
       if(disponible>=totalPagar){
           //Actualiza BD del cliente
           cantidadActualStock= cantidadActualStock+cantidadComprar;
           System.out.println("Disponible antes:"+disponible);
           System.out.println("TotalPagar:"+totalPagar);
           disponible= disponible-totalPagar;
           System.out.println("Disponible nuevo:"+disponible);
           cliente.getComponentesHashTable().replace(
                   componente, cantidadActualStock);
           cliente.getComponentesHashTable().replace(
                   tipoPago, disponible ); 
           System.out.println("Disponible:"+cliente.getComponentesHashTable().get(Componentes.DINERO));
           System.out.println("Disponible:"+cliente.getComponentesHashTable().get(Componentes.ARMERIA));
           infoTField.setBackground(new Color(67,204,94));
           infoTField.setText("Compra exitosa");
           actualizarDineroAcero();
           cliente.getJuego().colocarCantidadesComponentes();
       }
       else{
           infoTField.setBackground(new Color(219,58,52));
           infoTField.setText("No tiene recursos sufiecientes");
           actualizarDineroAcero();
       }
       
    }//GEN-LAST:event_btn_ComprarActionPerformed


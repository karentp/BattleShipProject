    public void actualizarDatos(Componentes componente, String componenteActual){
       precio= cliente.getComponentInfo(componente).getPrecio();
       cantidad=1;
       componenteTField.setText(componenteActual);
       precioTField.setText(Integer.toString(precio));
       cantidadTField.setText(Integer.toString(cantidad));
       actualizarTotalDinero(cantidad);
    }
    
    public void actualizarTotalDinero(int cantidad){
        int precioA= Integer.parseInt(precioTField.getText());
        int total= precioA*cantidad;
        totalTField.setText(Integer.toString(total));
        
    }
    
    public void actualizarDineroAcero(){
        dineroDisponible= cliente.getComponentesHashTable().get(Componentes.DINERO);
        aceroDisponible= cliente.getComponentesHashTable().get(Componentes.ACERO);
        dineroDisponibleTField.setText(Integer.toString(dineroDisponible));
        aceroDisponibleTField.setText(Integer.toString(aceroDisponible));
    }

